const RTL_LANGS = ['ar'];
function applyLang(code) {
  const tr = I18N[code] || I18N.fr;
  localStorage.setItem('az_lang', code);
  document.documentElement.dir  = RTL_LANGS.includes(code) ? 'rtl' : 'ltr';
  document.documentElement.lang = code;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (tr[key] === undefined) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = tr[key];
    else if (el.tagName === 'OPTION') el.textContent = tr[key];
    else el.textContent = tr[key];
  });
  document.querySelectorAll('.lang-btn').forEach(b =>
    b.classList.toggle('active', b.dataset.lang === code)
  );
  window.dispatchEvent(new CustomEvent('langchange', { detail: { lang: code, t: tr } }));
}

